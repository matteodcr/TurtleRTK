import {observer} from 'mobx-react-lite';
import React from 'react';
import {
  SafeAreaView,
  View,
  Text,
  StyleSheet,
  PermissionsAndroid,
} from 'react-native';
import {Button} from 'react-native-paper';
import {useStoreContext} from '../../../fc/Store';

interface Props {
  navigation: any;
}
export default observer(function LogScreen({navigation}: Props) {
  const store = useStoreContext();

  React.useEffect(() => {
    return navigation.addListener('focus', async () => {
      store.logManager.handleRecordingDirectory();
      try {
        const granted = await PermissionsAndroid.request(
          PermissionsAndroid.PERMISSIONS.WRITE_EXTERNAL_STORAGE,
          {
            title: 'Cool Photo App Camera Permission',
            message: 'Your app needs permission.',
            buttonNeutral: 'Ask Me Later',
            buttonNegative: 'Cancel',
            buttonPositive: 'OK',
          },
        );
        if (granted === PermissionsAndroid.RESULTS.GRANTED) {
          return true;
        } else {
          console.log('Camera permission denied');
          return false;
        }
      } catch (err) {
        console.warn(err);
        return false;
      }
    });
  });
  const renderHeaderTab = () => {
    return (
      <View style={styles.headerTab}>
        <Text
          style={{
            marginLeft: 15,
            fontSize: 18,
            fontWeight: 'bold',
            color: 'white',
          }}>
          Recording Screen
        </Text>
      </View>
    );
  };

  return (
    <SafeAreaView style={styles.container}>
      {renderHeaderTab()}
      <View>
        <Button onPress={() => store.logManager.scan(store.logManager.root)}>
          Scan
        </Button>
        <Button onPress={() => store.logManager.write('StylÃ©')}>
          Write file
        </Button>
      </View>
    </SafeAreaView>
  );
});

